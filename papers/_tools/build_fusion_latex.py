def build_hub(hub: dict, papers_root: Path, run_extract: bool) -> None:
    hub_dir = papers_root / hub["hub_dir"]
    if not hub_dir.is_dir():
        raise FileNotFoundError(hub_dir)

    sources = hub["sources"]
    chapters_tex = []

    for src in sources:
        subdir = src["subdir"]
        main_tex = src["main_tex"]
        chapter_title = src["chapter"]
        full_main = hub_dir / subdir / main_tex
        if not full_main.is_file():
            raise FileNotFoundError(full_main)

        if run_extract:
            cmd = [sys.executable, str(EXTRACT), str(full_main)]
            if src.get("include_filtered_preamble", True):
                cmd.append("--with-preamble")
            r = subprocess.run(
                cmd,
                cwd=str(SCRIPT_DIR),
                capture_output=True,
                text=True,
            )
            if r.returncode != 0:
                print(r.stderr or r.stdout, file=sys.stderr)
                raise RuntimeError(f"extract failed for {full_main}")

        body_name, sub_line = subimport_tex_fragment(subdir, main_tex)
        body_path = hub_dir / subdir / body_name
        if not body_path.is_file():
            raise FileNotFoundError(f"Missing body fragment (run with extract): {body_path}")

        chapters_tex.append(
            f"\\chapter{{{latex_escape_comment(chapter_title)}}}\n{sub_line}\n\\clearpage\n\n"
        )

    title = hub["title"]
    master_name = hub["master_tex"]
    hub_id = hub.get("id", "")
    extra = HUB_EXTRA_LATEX.get(hub_id, "")
    preamble = MASTER_PREAMBLE.replace("%HUB_EXTRA%", extra)
    content = preamble.replace("%TITLE%", latex_escape_comment(title)) + "".join(chapters_tex) + "\\end{document}\n"
    out_path = hub_dir / master_name
    out_path.write_text(content, encoding="utf-8")
    print(f"Wrote {out_path}")


def main() -> int:
    import argparse

    ap = argparse.ArgumentParser()
    ap.add_argument("--no-extract", action="store_true", help="Only write masters; assume bodies exist")
    args = ap.parse_args()

    data = json.loads(MANIFEST.read_text(encoding="utf-8"))
    root = (SCRIPT_DIR / data.get("papers_root", "..")).resolve()
    if root != PAPERS_ROOT:
        pass  # allow override
    root = PAPERS_ROOT

    for hub in data["hubs"]:
        build_hub(hub, root, run_extract=not args.no_extract)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

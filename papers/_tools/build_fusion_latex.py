"""
Read fusion_manifest.json, extract bodies for each listed source, and write *_Fusion_master.tex per hub.
"""
from __future__ import annotations

import json
import subprocess
import sys
from pathlib import Path

SCRIPT_DIR = Path(__file__).resolve().parent
PAPERS_ROOT = (SCRIPT_DIR / "..").resolve()
MANIFEST = SCRIPT_DIR / "fusion_manifest.json"
EXTRACT = SCRIPT_DIR / "extract_fusion_body.py"


def latex_escape_comment(s: str) -> str:
    return s.replace("%", "\\%")


def subimport_tex_fragment(subdir: str, main_tex: str) -> str:
    """Return filename of body fragment and LaTeX subimport line."""
    stem = Path(main_tex).stem
    body_name = f"_fusion_body__{stem}.tex"
    # import package expects directory with trailing slash
    d = subdir.replace("\\", "/").strip("/") + "/"
    return body_name, f"\\subimport{{{d}}}{{{body_name}}}"


